package krn;

public class swap {
	public static void main (String [] args)
	{
	int a=10;
	int b=20;
	
		System.out.println(" before swapping a  " + a  );
		
		System.out.println(" before swapping b  " + b  );
		
		int temp = a;
		
		     a    = b;
		     
		     b  = temp;
		     
		   
			System.out.println(" \n after swapping a " + a );
			
				System.out.println(" after swapping b " + b   );
	}

}


